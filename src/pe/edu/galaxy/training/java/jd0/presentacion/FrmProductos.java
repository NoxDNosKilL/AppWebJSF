    private void listarProductos() {
        String cab[] = {"CÃ³digo", "Nombre", "Precio", "Stock"};
        DefaultTableModel dtm = new DefaultTableModel(cab, 0);
        tblProductos.setModel(dtm);
        try {
            ProductoDAO productoDAO = new ProductoDAOImpl();
            List<Producto> productos = productoDAO.listar(txtNombreBusqueda.getText());
            productos.forEach(p -> {
                Vector fila = new Vector();
                fila.add(p.getId());
                fila.add(p.getNombre());
                fila.add(p.getPrecio());
                fila.add(p.getStock());
                dtm.addRow(fila);
            });
        } catch (Exception e) {
            System.err.println(e.getMessage());
        }
    }

    private void grabar() {
        try {
            ProductoDAO productoDAO = new ProductoDAOImpl();

            Producto producto = new Producto();
            producto.setNombre(txtNombre.getText());
            producto.setPrecio(Double.valueOf(txtPrecio.getText()));
            producto.setStock(Integer.valueOf(txtStock.getText()));

            if (productoDAO.agregar(producto)) {
                JOptionPane.showMessageDialog(rootPane, "Exito de registro", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                this.listarProductos();
                this.limpiar();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Error de registro", "Alerta", JOptionPane.WARNING_MESSAGE);
            }
        } catch (ProductoException e) {
            System.err.println(e.getMessage());
        } catch (DAOException e) {

            System.err.println(e.getMessage());
        }

    }

    private void limpiar() {
        txtNombre.setText("");
        txtPrecio.setText("");
        txtStock.setText("");
        txtNombre.requestFocus();
    }

